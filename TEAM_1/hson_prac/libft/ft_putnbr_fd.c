#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	*c;

	if (fd < 0)
		return ;
	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	c = ft_itoa(n);
	write(fd, c, ft_strlen(c));
}
